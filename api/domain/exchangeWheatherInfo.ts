// 天気コードから天気を返す関数
export function exchangeWheatherCode(wx: number): string {
    return WeatherCodeMap[wx] ?? "No Data";
}

// 天気コードから画像のURLを返す関数
export function getWeatherIconUrl(wx: number): string {
    return `https://tpf.weathernews.jp/wxicon/152/${wx}.png`;
}

// 風向きコードから風向きを返す関数
export function exchangeWindDirection(wnddir: number): string {
    return WindDirectionMap[wnddir] ?? "不明";
}

